    public void tampilData(String where) {
  try {
    
    tabModel.getDataVector().removeAllElements();
    tabModel.fireTableDataChanged();
    rs = st.executeQuery("SELECT * FROM product " + where);
    
    while (rs.next()) {
      Object[] data = {
        rs.getString("NAMA ITEM"),
        rs.getString("HARGA ITEM"),
        
      };
        
        tabModel.addRow(data);
    }
  } catch(Exception e) {
    e.printStackTrace();
  }
}
    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
       DefaultTableModel model = (DefaultTableModel) tabelItemA.getModel();
       if(tabelItemA.getSelectedRowCount() == 1){
           String name = txtNamaItem1.getText();
           String harga =txtHargaItem1.getText();
           
           //set u
           model.setValueAt(name, tabelItemA.getSelectedRow(), 0);
           model.setValueAt(harga, tabelItemA.getSelectedRow(), 1);
           
           JOptionPane.showMessageDialog(this, "Update Successfully...");
           
       }else{
           if(tabelItemA.getRowCount()==0){
               JOptionPane.showMessageDialog(this,"Table is Empty...");
           }else{
               JOptionPane.showMessageDialog(this,"Please select single Row for update...");
           }

       }
    

        
 
 
    }//GEN-LAST:event_btnupdateActionPerformed

